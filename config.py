FEATURE_COLS = ["GLD", "DGS10", "CPI", "DXY", "SP500", "VIX"]
TARGET_COL = "GLD"   # 預測標的：金價

# ---------------------------------------------------------------------------
# CPI 公布日延遲（避免前視偏誤 look-ahead bias）
# ---------------------------------------------------------------------------
# CPIAUCSL 在 FRED 以「所屬月份的第一天」為索引（例：2023-01-01 = 一月份 CPI）。
# 但該數據實際上要到「隔月中旬」才由 BLS 公布（例：一月 CPI 約於 2 月 14 日發布）。
# 若直接用所屬月份當生效日，等於在月初就「偷看到」尚未公布的數字 → 前視偏誤。
# 因此把 CPI 的生效日往後延約「1 個月又 14 天」，近似真實公布日，再 forward fill。
CPI_PUBLISH_LAG_MONTHS = 1
CPI_PUBLISH_LAG_DAYS = 14

# ---------------------------------------------------------------------------
# 模型 / 序列參數
# ---------------------------------------------------------------------------
LOOKBACK = 60          # 用過去 60 天
